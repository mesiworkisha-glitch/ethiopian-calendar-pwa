function getLiturgicalSeason(ey, em, ed, bh) {
    let d = ethiopianDayOfYear(em, ed);
    let nenewe_day = (bh.feasts.nenewe.m - 1) * 30 + bh.feasts.nenewe.d, abiy_day = (bh.feasts.abiy.m - 1) * 30 + bh.feasts.abiy.d;
    let tensae_day = (bh.feasts.tensae.m - 1) * 30 + bh.feasts.tensae.d, erget_day = (bh.feasts.erget.m - 1) * 30 + bh.feasts.erget.d;
    let pentecost_day = (bh.feasts.parakletos.m - 1) * 30 + bh.feasts.parakletos.d;

    if (d >= 1 && d <= 7) return "ዘመነ ዮሐንስ"; if (d === 8) return "ዘካርያስ"; if (d >= 9 && d <= 15) return "ዘመነ ፍሬ";
    if (d >= 16 && d <= 25) return "ዘመነ መስቀል"; if (d >= 26 && d <= 65) return "ዘመነ ጽጌ"; if (d >= 66 && d <= 96) return "ዘመነ አስተምሕሮ";
    if (d >= 97 && d <= 103) return "ዘመነ ስብከት"; if (d >= 104 && d <= 110) return "ዘመነ ብርሃን"; if (d >= 111 && d <= 116) return "ዘመነ ኖላዊ";
    if (d === 117) return "ዘመነ መርዓዊ"; if (d === 118) return "አማኑኤል"; if (d >= 119 && d <= 126) return "ዘመነ ልደት";
    if (d >= 127 && d <= 129) return "ናዝሬት"; if (d === 130) return "ገሐድ"; if (d >= 131 && d < nenewe_day) return "ዘመነ ጥምቀት";
    if (d >= nenewe_day && d < nenewe_day + 3) return "ዘመነ ነነዌ"; if (d >= nenewe_day + 3 && d < abiy_day) return "ዘመነ መርዓዊ";
    if (d >= abiy_day && d < tensae_day) return "ዘመነ ጾም"; if (d >= tensae_day && d < erget_day) return "ዘመነ ትንሣኤ";
    if (d >= erget_day && d < pentecost_day) return "ዘመነ ዕርገት"; if (d >= pentecost_day && d <= 286) return "ዘመነ ጰራቅሊጦስ";
    if (d >= 287 && d <= 295) return "ዘመነ አስተምሕሮ"; if (d >= 296 && d <= 318) return "ደመና፣ ዘርዕ፣ ዝናም";
    if (d >= 319 && d <= 339) return "መብረቅ፣ ባሕር"; if (d >= 340 && d <= 357) return "ዐይነ ኵሉ፣ ዕጕለ ቋዓት";
    return "ጎሕ፣ ነግሕ";
}

function getSeasons(ey, em, ed, bh) {
    let dayNum = (em - 1) * 30 + ed, climatic = dayNum >= 26 && dayNum <= 115 ? t('season_autumn') : dayNum >= 116 && dayNum <= 205 ? t('season_summer') : dayNum >= 206 && dayNum <= 295 ? t('season_spring') : t('season_winter');
    let fNenewe = (bh.feasts.nenewe.m - 1) * 30 + bh.feasts.nenewe.d, fAbiy = (bh.feasts.abiy.m - 1) * 30 + bh.feasts.abiy.d;
    let fTensae = (bh.feasts.tensae.m - 1) * 30 + bh.feasts.tensae.d, fHawariat = (bh.feasts.hawaryat.m - 1) * 30 + bh.feasts.hawaryat.d;
    let fPentecost = (bh.feasts.parakletos.m - 1) * 30 + bh.feasts.parakletos.d;

    let fasting = t('fast_none');
    if (dayNum >= fAbiy && dayNum < fTensae) fasting = t('fast_abiy');
    else if (dayNum >= 75 && dayNum <= 118) fasting = t('fast_nebiyat');
    else if (dayNum >= 331 && dayNum <= 345) fasting = t('fast_filseta');
    else if (dayNum >= fHawariat && dayNum <= 305) fasting = t('fast_hawaryat');
    else if (dayNum >= fNenewe && dayNum < fNenewe + 3) fasting = t('fast_nenewe');
    else if (dayNum === 130) fasting = t('fast_gehad');
    else if (dayNum >= fTensae && dayNum <= fPentecost) fasting = t('fast_hamsa');
    else { let wd = ethToGregorian(ey, em, ed).getDay(); if (wd === 3 || wd === 5) fasting = t('fast_dihnet'); }

    let progress = "", ranges = [ {n: t('fast_nenewe'), s: fNenewe, l: 3}, {n: t('fast_abiy'), s: fAbiy, l: fTensae - fAbiy}, {n: t('fast_nebiyat'), s: 75, l: 44}, {n: t('fast_hawaryat'), s: fHawariat, l: 305 - fHawariat + 1}, {n: t('fast_filseta'), s: 331, l: 15} ];
    for (let r of ranges) { if (dayNum >= r.s && dayNum < r.s + r.l) { progress = `(${r.n}: ${fNum(dayNum - r.s + 1)} / ${fNum(r.l)})`; break; } }
    return { climatic, liturgical: getLiturgicalSeason(ey, em, ed, bh), fasting, progress };
}

function getUpcomingEvents(ey, em, ed, bh) {
    let todayNum = (em - 1) * 30 + ed, events = [], m = getMonths();
    [[1, "hol_enkutatash"], [17, "hol_meskel"], [75, "fast_nebiyat"], [130, "fast_gehad"], [131, "hol_timkat"], [331, "fast_filseta"], [345, "hol_filseta_maryam"]].forEach(e => events.push(e));
    events.push([mod(ey, 4) === 0 ? 118 : 119, "hol_genna"]);
    for (const [internalKey, dateObj] of Object.entries(bh.feasts)) {
        let i18nKey = ["nenewe", "abiy", "hawaryat", "dihnet"].includes(internalKey) ? `fast_${internalKey}` : `fest_${internalKey}`;
        events.push([(dateObj.m - 1) * 30 + dateObj.d, i18nKey]);
    }
    events.sort((a,b) => a[0] - b[0]);
    for (let i = 0; i < events.length; i++) {
        if (events[i][0] > todayNum) {
            let daysLeft = events[i][0] - todayNum, targetEm = Math.floor((events[i][0] - 1) / 30) + 1, targetEd = ((events[i][0] - 1) % 30) + 1;
            let eventName = t(events[i][1]), dateStr = `${m[targetEm]} ${fNum(targetEd)}`;
            return events[i][1].startsWith('fast_') ? `${eventName} (${dateStr}) — በ${fNum(daysLeft)} ቀናት ውስጥ ይጀምራል` : `${eventName} (${dateStr}) — በ${fNum(daysLeft)} ቀናት ውስጥ`;
        }
    }
    return "";
}

