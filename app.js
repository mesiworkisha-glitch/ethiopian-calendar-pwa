function setupPeriodic() {
    const form = document.getElementById('periodic-form'); if (!form) return;
    let memoryFallback = { periods: [], cycle_len: 28, period_len: 5 }, storageBlocked = false;
    const loadData = () => { try { return JSON.parse(localStorage.getItem('periodic_tracker_data')) || { periods: [], cycle_len: 28, period_len: 5 }; } catch (e) { storageBlocked = true; return memoryFallback; } };
    const saveData = (d) => { try { localStorage.setItem('periodic_tracker_data', JSON.stringify(d)); } catch (e) { storageBlocked = true; memoryFallback = d; } };
    const updateUI = () => {
      try {
        let data = loadData(), out = document.getElementById('periodic-output'); if (!out) return;
        if (!data.periods.length) { out.innerHTML = `<p>${t('no_data')}</p>`; return; }
        let periods = data.periods.map(p => { let [py, pm, pd] = p.split('-'); return new Date(parseInt(py), parseInt(pm) - 1, parseInt(pd), 0, 0, 0, 0); }).sort((a, b) => a - b);
        let last = periods[periods.length - 1], today = new Date(); today.setHours(0, 0, 0, 0); last.setHours(0, 0, 0, 0);
        let cycleDay = Math.floor((today - last) / 86400000) + 1, nextStart = new Date(last.getTime() + data.cycle_len * 86400000), daysUntilNext = Math.round((nextStart - today) / 86400000), eNext = gregorianToEthiopian(nextStart.getFullYear(), nextStart.getMonth() + 1, nextStart.getDate());
        let html = `<h3>${t('cycle_details')}</h3><p><strong>${t('cycle_day_lbl')}:</strong> ${fNum(cycleDay)}</p><p><strong>${t('next_period_est')}:</strong> ${getMonths()[eNext.em]} ${fNum(eNext.ed)}, ${fNum(eNext.ey)} (${formatDate(nextStart)}) — <strong>${fNum(daysUntilNext)} ${t('days_left')}</strong></p>`;
        out.innerHTML = storageBlocked ? `<p style="color:#a66;"><em>${t('err_generic')}</em></p>` + html : html;
      } catch (err) { let out = document.getElementById('periodic-output'); if (out) out.innerHTML = `<p style="color:red;">${t('err_generic')}</p>`; }
    };

    form.addEventListener('submit', (e) => {
        e.preventDefault(); let out = document.getElementById('periodic-output');
        try {
            let y = parseInt(document.getElementById('per-year').value), m = parseInt(document.getElementById('per-month').value), d = parseInt(document.getElementById('per-day').value), cycleLen = parseInt(document.getElementById('per-cycle').value), periodLen = parseInt(document.getElementById('per-len').value);
            if (!y || !m || !d) { out.innerHTML = `<p style="color:red;">${t('err_enter_date')}</p>`; return; }
            if (!Number.isFinite(y) || Math.abs(y) > 200000) { out.innerHTML = `<p style="color:red;">${t('err_invalid_year')}</p>`; return; }
            if (m < 1 || m > 13) { out.innerHTML = `<p style="color:red;">${t('err_invalid_month_eth')}</p>`; return; }
            if (d < 1 || d > getMonthLength(y, m)) { out.innerHTML = `<p style="color:red;">${t('err_invalid_day')}</p>`; return; }
            if (!cycleLen || cycleLen < 15 || cycleLen > 60) { out.innerHTML = `<p style="color:red;">${t('err_cycle_range')}</p>`; return; }
            if (!periodLen || periodLen < 1 || periodLen > 15) { out.innerHTML = `<p style="color:red;">${t('err_period_range')}</p>`; return; }

            let g = ethToGregorian(y, m, d), today = new Date(); today.setHours(0, 0, 0, 0); let gAtMidnight = new Date(g); gAtMidnight.setHours(0, 0, 0, 0);
            if (gAtMidnight > today) { out.innerHTML = `<p style="color:red;">${t('err_future_date')}</p>`; return; }
            let data = loadData(); data.cycle_len = cycleLen; data.period_len = periodLen;
            let iso = `${g.getFullYear()}-${String(g.getMonth()+1).padStart(2,'0')}-${String(g.getDate()).padStart(2,'0')}`;
            if (!data.periods.includes(iso)) data.periods.push(iso); data.periods.sort(); saveData(data); updateUI();
        } catch (err) { out.innerHTML = `<p style="color:red;">${t('err_generic')}</p>`; }
    });

    const clearBtn = document.getElementById('btn-clear-periodic');
    if (clearBtn) clearBtn.addEventListener('click', () => { try { localStorage.removeItem('periodic_tracker_data'); } catch (e) { storageBlocked = true; memoryFallback = { periods: [], cycle_len: 28, period_len: 5 }; } updateUI(); });
    updateUI();
}

function setupAgeCalc() {
    const btn = document.getElementById('btn-calc-age'); if (!btn) return;
    btn.addEventListener('click', () => {
        let out = document.getElementById('age-output');
        try {
            let bY = parseInt(document.getElementById('age-year').value), bM = parseInt(document.getElementById('age-month').value), bD = parseInt(document.getElementById('age-day').value);
            if (!bY || !bM || !bD) { out.innerHTML = `<p style="color:red;">${t('err_enter_date')}</p>`; return; }
            if (!Number.isFinite(bY) || Math.abs(bY) > 200000) { out.innerHTML = `<p style="color:red;">${t('err_invalid_year')}</p>`; return; }
            if (bM < 1 || bM > 13) { out.innerHTML = `<p style="color:red;">${t('err_invalid_month_eth')}</p>`; return; }
            if (bD < 1 || bD > getMonthLength(bY, bM)) { out.innerHTML = `<p style="color:red;">${t('err_invalid_day')}</p>`; return; }

            let now = new Date(), curEth = gregorianToEthiopian(now.getFullYear(), now.getMonth() + 1, now.getDate());
            let cY = curEth.ey, cM = curEth.em, cD = curEth.ed, bJdn = ethiopianToJdn(bY, bM, bD), cJdn = ethiopianToJdn(cY, cM, cD);
            if (bJdn > cJdn) { out.innerHTML = `<p style="color:red;">${t('err_future_date')}</p>`; return; }

            let years = cY - bY, annivDay = Math.min(bD, getMonthLength(bY + years, bM)), annivJdn = ethiopianToJdn(bY + years, bM, annivDay);
            if (annivJdn > cJdn) { years--; annivDay = Math.min(bD, getMonthLength(bY + years, bM)); annivJdn = ethiopianToJdn(bY + years, bM, annivDay); }
            let months = 0, curY = bY + years, curM = bM, curJdn = annivJdn;
            for (let i = 0; i < 13; i++) {
                let nextM = curM + 1, nextY = curY; if (nextM > 13) { nextM = 1; nextY++; }
                let nextDay = Math.min(bD, getMonthLength(nextY, nextM)), nextJdn = ethiopianToJdn(nextY, nextM, nextDay);
                if (nextJdn > cJdn) break; months++; curY = nextY; curM = nextM; curJdn = nextJdn;
            }
            out.innerHTML = `<h3>${t('age_result')}</h3><p><strong>${fNum(years)} ${t('age_years')}, ${fNum(months)} ${t('age_months')}, ${fNum(cJdn - curJdn)} ${t('txt_days')}</strong></p><p>${t('total_life_days')}: ${fNum(cJdn - bJdn)}</p>`;
        } catch (err) { out.innerHTML = `<p style="color:red;">${t('err_generic')}</p>`; }
    });
}

