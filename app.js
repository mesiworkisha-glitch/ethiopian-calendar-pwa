                let weeks = Math.floor(elapsedDays / 7), days = elapsedDays % 7;
                gestation = `${fNum(weeks)} ${t('txt_weeks')} ${t('txt_and')} ${fNum(days)} ${t('txt_days')}`;
                trimester = weeks < 13 ? t('trim_1') : weeks < 27 ? t('trim_2') : t('trim_3');
